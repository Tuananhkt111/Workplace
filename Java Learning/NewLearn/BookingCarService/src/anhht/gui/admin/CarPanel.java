    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        try {
            int row = tblViewCar.getSelectedRow();
            String carSerial = (String) tblModelCar.getValueAt(row, 0);
            CarDAO carDAO = new CarDAO();
            if (carDAO.delete(carSerial)) {
                JOptionPane.showMessageDialog(null, "Delete success");
                btnDelete.setEnabled(false);
                if (txtSearch.getText().equals("")) {
                    btnSearch.doClick();
                } else {
                    tblModelCar.setRowCount(0);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Delete failed");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Please select car");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertActionPerformed
        // TODO add your handling code here:
        String carSerial = txtSerial.getText();
        String name = txtName.getText();
        boolean isAvailable = rdTrue.isSelected();
        int seats = Integer.parseInt((String) cbSeats.getSelectedItem());
        CarDAO carDAO = new CarDAO();
        if (validate(carSerial, name)) {
            if (carDAO.insert(new CarDTO(carSerial, name, seats, isAvailable))) {
                JOptionPane.showMessageDialog(null, "Insert success");
                btnDelete.setEnabled(false);
                txtSearch.setText("");
                btnSearch.doClick();
                btnReset.doClick();
            } else {
                JOptionPane.showMessageDialog(null, "Insert failed");
            }

        }
    }//GEN-LAST:event_btnInsertActionPerformed

