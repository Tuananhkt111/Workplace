    private boolean validate(String carSerial, String name) {
        boolean check = true;
        CarDAO carDAO = new CarDAO();
        String serialPattern = "\\d{2}[A-Z]\\d{5}";
        if (!carSerial.matches(serialPattern)) {
            msgSerial.setText("Serial format xxDxxxxx. Ex: 59G22132");
            check = false;
        } else if (carDAO.findExisting(carSerial)) {
            msgSerial.setText("Serial existed.");
            check = false;
        } else {
            msgSerial.setText("");
        }
        if (name.equals("")) {
            msgName.setText("Name is not empty.");
            check = false;
        } else {
            msgName.setText("");
        }
        return check;
    }
