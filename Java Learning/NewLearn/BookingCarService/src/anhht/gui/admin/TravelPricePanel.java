    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        String travelID = txtTravelID.getText();
        String des = txtDestination.getText();
        int limitTime = (Integer) spLimitTime.getValue();
        int distance = (Integer) spDistance.getValue();
        int seats = Integer.parseInt((String) cbSeats.getSelectedItem());
        int price = 0;
        try {
            price = Integer.parseInt(txtPrice.getText());
        } catch (NumberFormatException e) {
        }        
        TravelPriceDAO tpDAO = new TravelPriceDAO();
        if (validate(travelID, des, price)) {
            if (tpDAO.update(new TravelPriceDTO(travelID, des, limitTime, distance, seats, price))) {
                JOptionPane.showMessageDialog(null, "Update success");
                btnDelete.setEnabled(false);
                btnSearch.doClick();
                btnReset.doClick();
                txtTravelID.setText("");
            } else {
                JOptionPane.showMessageDialog(null, "Update failed");
            }
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private boolean validate(String travelID, String destination, int price){
        boolean check = true;
        if (travelID.equals("")) {
            check = false;
            JOptionPane.showMessageDialog(null, "Please choose travel price");
        } else {
            if(destination.equals("")) {
                msgDes.setText("Destination is not empty");
                check = false;
            } else {
                msgDes.setText("");
            }
            if (price < 1) {
                msgPrice.setText("Price is a positive number");
                check = false;
            } else {
                msgPrice.setText("");
            }
        }
        return check;
    }
    
    private void loadTravelPrice(String travelID) {
        TravelPriceDAO tpDAO = new TravelPriceDAO();
        TravelPriceDTO tpDTO = tpDAO.findByPrimaryKey(travelID);
        if (tpDTO != null) {
            txtTravelID.setText(tpDTO.getId());
            txtDestination.setText(tpDTO.getDestination());
            spLimitTime.setValue(tpDTO.getLimitTime());
            spDistance.setValue(tpDTO.getEstDistance());
            cbSeats.setSelectedItem(Integer.toString(tpDTO.getNumberOfSeats()));
            txtPrice.setText(Integer.toString(tpDTO.getPrice()));
        } else {
            JOptionPane.showMessageDialog(null, "Cannot load travel price information", "System warning", JOptionPane.INFORMATION_MESSAGE);
        }
    }
    
