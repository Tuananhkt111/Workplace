    private void tblViewTravelPriceMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblViewTravelPriceMouseClicked
        // TODO add your handling code here:
        try {
            int row = tblViewTravelPrice.getSelectedRow();
            String travelID = (String) tblModelTravelPrice.getValueAt(row, 0);
            loadTravelPrice(travelID);
            btnDelete.setEnabled(true);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Please select travel price");
        }
    }//GEN-LAST:event_tblViewTravelPriceMouseClicked

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        TravelPriceDAO tpDAO = new TravelPriceDAO();
        List<TravelPriceDTO> result = null;
        if (txtSearch.getText().equals("")) {
            result = tpDAO.findAll();
        } else {
            TravelPriceDTO tpDTO = tpDAO.findByPrimaryKey(txtSearch.getText());
            if (tpDTO != null) {
                result = new ArrayList<>();
                result.add(tpDTO);
            }
        }
        if (result != null && !result.isEmpty()) {
            showTravelPrice(result);
        } else {
            JOptionPane.showMessageDialog(null, "Not Found!");
            tblModelTravelPrice.setRowCount(0);
        }
        btnDelete.setEnabled(false);
        btnReset.doClick();
        txtTravelID.setText("");
    }//GEN-LAST:event_btnSearchActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        try {
            int row = tblViewTravelPrice.getSelectedRow();
            String travelID = (String) tblModelTravelPrice.getValueAt(row, 0);
            TravelPriceDAO tpDAO = new TravelPriceDAO();
            if (tpDAO.delete(travelID)) {
                JOptionPane.showMessageDialog(null, "Delete success");
                btnDelete.setEnabled(false);
                btnReset.doClick();
                txtTravelID.setText("");
                if (txtSearch.getText().equals("")) {
                    btnSearch.doClick();
                } else {
                    tblModelTravelPrice.setRowCount(0);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Delete failed. This travel price may contain some transaction.");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Please select travel price");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

