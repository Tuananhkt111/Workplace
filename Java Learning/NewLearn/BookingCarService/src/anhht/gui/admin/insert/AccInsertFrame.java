    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertActionPerformed
        // TODO add your handling code here:
        String username = txtUsername.getText();
        String pass = new String(txtPass.getPassword());
        String fullname = txtName.getText();
        String phone = txtPhone.getText();
        boolean sex = rdMale.isSelected();
        int age = 0;
        try {
            age = Integer.parseInt(txtAge.getText());
        } catch (NumberFormatException e) {
        }
        CustomerDAO accDAO = new CustomerDAO();
        if (validate(username, pass, fullname, phone, age)) {
            if (accDAO.insert(new CustomerDTO(username, pass, fullname, phone, sex, age))) {
                JOptionPane.showMessageDialog(null, "Insert success");
                btnDeleteMain.setEnabled(false);
                txtSearchMain.setText("");
                btnSearchMain.doClick();
                btnResetMain.doClick();
                txtUsernameMain.setText("");
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Insert failed");
            }

        }
    }//GEN-LAST:event_btnInsertActionPerformed

