    private boolean validate(String username, String pass, String fullname, String phone, int age) {
        boolean check = true;
        String usernamePattern = "[\\w]{7,}";
        String passPattern = "(?=.*\\d)(?=.*[a-zA-Z])[\\w]{8,}";
        String phonePattern = "\\d{10}";
        CustomerDAO accDAO = new CustomerDAO();
        if (!username.matches(usernamePattern)) {
            msgUsername.setText("Username must be 7 or more alphanumberic characters.\n");
            check = false;
        } else if (accDAO.findExisting(username)) {
            msgUsername.setText("Username is existed.");
            check = false;
        } else {
            msgUsername.setText("");
        }
        if (!pass.matches(passPattern)) {
            msgPass.setText("Password must be 8 or more alphanumberic characters");
            msgPass2.setText("including at least 1 letter and 1 numberic letter.");
            check = false;
        } else {
            msgPass.setText("");
            msgPass2.setText("");
        }
        if (fullname.equals("")) {
            msgName.setText("Full name is not empty.");
            check = false;
        } else {
            msgName.setText("");
        }
        if (!phone.matches(phonePattern)) {
            msgPhone.setText("Phone is the sequence of 10 digits.");
            check = false;
        } else {
            msgPhone.setText("");
        }

        if (age < 1) {
            msgAge.setText("Age is a positive number");
            check = false;
        } else {
            msgAge.setText("");
        }
        return check;
    }

