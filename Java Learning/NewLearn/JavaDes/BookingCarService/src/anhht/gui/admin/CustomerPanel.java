    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        CustomerDAO cusDAO = new CustomerDAO();
        List<CustomerDTO> result = null;
        if (txtSearch.getText().equals("")) {
            result = cusDAO.findAll();
        } else {
            CustomerDTO cusDTO = cusDAO.findByPrimaryKey(txtSearch.getText());
            if (cusDTO != null) {
                result = new ArrayList<>();
                result.add(cusDTO);
            }
        }
        if (result != null && !result.isEmpty()) {
            showCustomer(result);
        } else {
            JOptionPane.showMessageDialog(null, "Not Found!");
            tblModelCustomer.setRowCount(0);
        }
        btnDelete.setEnabled(false);
        btnReset.doClick();
        txtCusID.setText("");
    }//GEN-LAST:event_btnSearchActionPerformed

    private void tblViewCustomerMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblViewCustomerMouseClicked
        // TODO add your handling code here:
        try {
            int row = tblViewCustomer.getSelectedRow();
            String cusID = (String) tblModelCustomer.getValueAt(row, 0);
            loadCustomer(cusID);
            btnDelete.setEnabled(true);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Please select customer");
        }

    }//GEN-LAST:event_tblViewCustomerMouseClicked

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        try {
            int row = tblViewCustomer.getSelectedRow();
            String cusID = (String) tblModelCustomer.getValueAt(row, 0);
            CustomerDAO cusDAO = new CustomerDAO();
            int dialogBtn = JOptionPane.showConfirmDialog(null, "Are you sure want to remove?", "Warning", JOptionPane.YES_NO_OPTION);
            if (dialogBtn == JOptionPane.YES_OPTION) {
                if (cusDAO.delete(cusID)) {
                    JOptionPane.showMessageDialog(null, "Delete success");
                    btnDelete.setEnabled(false);
                    btnReset.doClick();
                    txtCusID.setText("");
                    if (txtSearch.getText().equals("")) {
                        btnSearch.doClick();
                    } else {
                        tblModelCustomer.setRowCount(0);
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Delete failed. This customer can contain some transactions.");
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Please select customer");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

