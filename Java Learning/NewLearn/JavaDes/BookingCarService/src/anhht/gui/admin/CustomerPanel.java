    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:

        String cusID = txtCusID.getText();
        String fullname = txtName.getText();
        String phone = txtPhone.getText();
        boolean sex = rdMale.isSelected();
        int age = 0;
        try {
            age = Integer.parseInt(txtAge.getText());
        } catch (NumberFormatException e) {
        }
        CustomerDAO cusDAO = new CustomerDAO();
        if (validate(cusID, fullname, phone, age)) {
            int dialogBtn = JOptionPane.showConfirmDialog(null, "Are you sure want to save changes?", "Warning", JOptionPane.YES_NO_OPTION);
            if (dialogBtn == JOptionPane.YES_OPTION) {
                if (cusDAO.update(new CustomerDTO(cusID, fullname, phone, sex, age))) {
                    JOptionPane.showMessageDialog(null, "Update success");
                    btnDelete.setEnabled(false);
                    btnSearch.doClick();
                    btnReset.doClick();
                    txtCusID.setText("");
                } else {
                    JOptionPane.showMessageDialog(null, "Update failed");
                }
            }
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

