    private void loadCustomer(String cusID) {
        CustomerDAO cusDAO = new CustomerDAO();
        CustomerDTO cusDTO = cusDAO.findByPrimaryKey(cusID);
        if (cusDTO != null) {
            txtCusID.setText(cusDTO.getId());
            txtName.setText(cusDTO.getName());
            if (cusDTO.isSex()) {
                rdMale.setSelected(true);
            } else {
                rdFemale.setSelected(true);
            }
            txtAge.setText(Integer.toString(cusDTO.getAge()));
            txtPhone.setText(cusDTO.getPhone());
        } else {
            JOptionPane.showMessageDialog(null, "Cannot load user information", "System warning", JOptionPane.INFORMATION_MESSAGE);
        }
    }

    private boolean validate(String id, String fullname, String phone, int age) {
        boolean check = true;
        String phonePattern = "\\d{10}";
        if (id.equals("")) {
            check = false;
            JOptionPane.showMessageDialog(null, "Please choose customer");
        } else {
            if (fullname.equals("")) {
                msgName.setText("Full name is not empty.");
                check = false;
            } else {
                msgName.setText("");
            }
            if (!phone.matches(phonePattern)) {
                msgPhone.setText("Phone is the sequence of 10 digits.");
                check = false;
            } else {
                msgPhone.setText("");
            }

            if (age < 1) {
                msgAge.setText("Age is a positive number");
                check = false;
            } else {
                msgAge.setText("");
            }
        }
        return check;
    }
