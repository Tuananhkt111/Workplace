    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        EmpDAO empDAO = new EmpDAO();
        List<EmployeeDTO> result = null;
        if (txtSearch.getText().equals("")) {
            result = empDAO.findAllEmployees();
        } else {
            EmployeeDTO empDTO = empDAO.findByPrimaryKey(txtSearch.getText());
            if (empDTO != null) {
                result = new ArrayList<>();
                result.add(empDTO);
            }
        }
        if (result != null && !result.isEmpty()) {
            showEmployee(result);
        } else {
            JOptionPane.showMessageDialog(null, "Not Found!");
            tblModelEmployee.setRowCount(0);
        }
        btnDelete.setEnabled(false);
        btnReset.doClick();
        txtEmpID.setText("");
    }//GEN-LAST:event_btnSearchActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        try {
            int row = tblViewEmployee.getSelectedRow();
            String empID = (String) tblModelEmployee.getValueAt(row, 0);
            EmpDAO empDAO = new EmpDAO();
            int dialogBtn = JOptionPane.showConfirmDialog(null, "Are you sure want to remove?", "Warning", JOptionPane.YES_NO_OPTION);
            if (dialogBtn == JOptionPane.YES_OPTION) {
                if (empDAO.delete(empID)) {
                    JOptionPane.showMessageDialog(null, "Delete success");
                    btnDelete.setEnabled(false);
                    btnReset.doClick();
                    txtEmpID.setText("");
                    if (txtSearch.getText().equals("")) {
                        btnSearch.doClick();
                    } else {
                        tblModelEmployee.setRowCount(0);
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Delete failed. This employee can be in some transactions");
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Please select employee");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

