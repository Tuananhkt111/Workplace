    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:

        String empID = txtEmpID.getText();
        String fullname = txtName.getText();
        String phone = txtPhone.getText();
        boolean sex = rdMale.isSelected();
        boolean available = rdTrue.isSelected();
        int age = 0;
        int certificate = 0;
        try {
            age = Integer.parseInt(txtAge.getText());
        } catch (NumberFormatException e) {
        }
        try {
            certificate = (Integer) spCertificate.getValue();
        } catch (NumberFormatException e) {
        }
        EmpDAO empDAO = new EmpDAO();
        if (validate(empID, fullname, phone, age)) {
            int dialogBtn = JOptionPane.showConfirmDialog(null, "Are you sure want to save changes?", "Warning", JOptionPane.YES_NO_OPTION);
            if (dialogBtn == JOptionPane.YES_OPTION) {
                if (empDAO.update(new EmployeeDTO(empID, fullname, phone, sex, available, age, certificate))) {
                    JOptionPane.showMessageDialog(null, "Update success");
                    btnDelete.setEnabled(false);
                    btnSearch.doClick();
                    btnReset.doClick();
                    txtEmpID.setText("");
                } else {
                    JOptionPane.showMessageDialog(null, "Update failed");
                }
            }
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

