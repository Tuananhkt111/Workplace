    private void loadEmployee(String empID) {
        EmpDAO empDAO = new EmpDAO();
        EmployeeDTO empDTO = empDAO.findByPrimaryKey(empID);
        if (empDTO != null) {
            txtEmpID.setText(empDTO.getId());
            txtName.setText(empDTO.getName());
            if (empDTO.isSex()) {
                rdMale.setSelected(true);
            } else {
                rdFemale.setSelected(true);
            }
            txtAge.setText(Integer.toString(empDTO.getAge()));
            spCertificate.setValue(empDTO.getCertificate());
            txtPhone.setText(empDTO.getPhone());
            if (empDTO.isIsAvailable()) {
                rdTrue.setSelected(true);
            } else {
                rdFalse.setSelected(true);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Cannot load employee information", "System warning", JOptionPane.INFORMATION_MESSAGE);
        }
    }

    private boolean validate(String empID, String fullname, String phone, int age) {
        boolean check = true;
        String phonePattern = "\\d{10}";
        if (empID.equals("")) {
            check = false;
            JOptionPane.showMessageDialog(null, "Please choose employee");
        } else {
            if (fullname.equals("")) {
                msgName.setText("Full name is not empty.");
                check = false;
            } else {
                msgName.setText("");
            }
            if (!phone.matches(phonePattern)) {
                msgPhone.setText("Phone is the sequence of 10 digits.");
                check = false;
            } else {
                msgPhone.setText("");
            }

            if (age < 1) {
                msgAge.setText("Age is a positive number");
                check = false;
            } else {
                msgAge.setText("");
            }
        }
        return check;
    }

