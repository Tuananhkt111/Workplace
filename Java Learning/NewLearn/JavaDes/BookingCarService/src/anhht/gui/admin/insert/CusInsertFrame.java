    private boolean validate(String cusID, String fullname, String phone, int age) {
        boolean check = true;
        String cusPattern = "\\d{9}";
        String phonePattern = "\\d{10}";
        CustomerDAO cusDAO = new CustomerDAO();
        if(!cusID.matches(cusPattern)) {
            msgCusID.setText("CusID is the sequence of 9 digits.");
            check = false;
        } else if(cusDAO.findExisting(cusID)) {
            msgCusID.setText("CusID existed.");
        } 
        else {
            msgCusID.setText("");
        }
        if (fullname.equals("")) {
            msgName.setText("Full name is not empty.");
            check = false;
        } else {
            msgName.setText("");
        }
        if (!phone.matches(phonePattern)) {
            msgPhone.setText("Phone is the sequence of 10 digits.");
            check = false;
        } else {
            msgPhone.setText("");
        }

        if (age < 1) {
            msgAge.setText("Age is a positive number");
            check = false;
        } else {
            msgAge.setText("");
        }
        return check;
    }

