    private void loadCarCategory() {
        CarCategoryDAO carCateDAO = new CarCategoryDAO();
        List<CarCategoryDTO> result = carCateDAO.findAll();
        cbSeats.addItem("All");
        for (CarCategoryDTO carCategoryDTO : result) {
            cbSeats.addItem(Integer.toString(carCategoryDTO.getNumberOfSeats()));
        }
    }

    private void setDefaultDuration(int min) {
        SpinnerNumberModel snm = (SpinnerNumberModel) spDuration.getModel();
        snm.setMinimum(min);
        snm.setMaximum(120);
        snm.setValue(min);
        snm.setStepSize(1);
        ((JSpinner.DefaultEditor) spDuration.getEditor()).getTextField().setEditable(false);
    }

    private void setDefaultStartTime() {
        SpinnerDateModel sdm = (SpinnerDateModel) spStartTime.getModel();
        Date minDate = new Date(Calendar.getInstance().getTimeInMillis());
        Date currDate = new Date(Calendar.getInstance().getTimeInMillis() + 300000);
        sdm.setStart(minDate);
        sdm.setValue(currDate);
        ((JSpinner.DefaultEditor) spStartTime.getEditor()).getTextField().setEditable(false);
    }

    private void loadDiscount() {
        try {
            int row = tblViewTravelPrice.getSelectedRow();
            String travelID = (String) tblModelTravelPrice.getValueAt(row, 0);
            TravelPriceDAO tpDAO = new TravelPriceDAO();
            TravelPriceDTO tpDTO = tpDAO.findByPrimaryKey(travelID);
            CarCategoryDAO ccDAO = new CarCategoryDAO();
            CarCategoryDTO ccDTO = ccDAO.findByPrimaryKey(tpDTO.getNumberOfSeats());
            TravelTransactionDAO ttDAO = new TravelTransactionDAO();
            List<TravelTransactionDTO> result = ttDAO.findByCusID(cusID);
            if (result.size() % 4 == 0 && !result.isEmpty()) {
                lblDiscount.setText("Discount " + Integer.toString(Math.round(ccDTO.getDiscount() * 100)) + "%");
            } else {
                lblDiscount.setText("");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

    private void changePrice() {
        try {
            int result;
            int row = tblViewTravelPrice.getSelectedRow();
            int duration = (Integer) spDuration.getValue();
            float discount = 0;
            if (!lblDiscount.getText().equals("")) {
                try {
                    discount = Float.parseFloat(lblDiscount.getText().substring(10, lblDiscount.getText().length() - 1)) / 100;
                } catch (NumberFormatException e) {
                }
            }
            String travelID = (String) tblModelTravelPrice.getValueAt(row, 0);
            TravelPriceDAO tpDAO = new TravelPriceDAO();
            TravelPriceDTO tpDTO = tpDAO.findByPrimaryKey(travelID);
            CarCategoryDAO ccDAO = new CarCategoryDAO();
            CarCategoryDTO ccDTO = ccDAO.findByPrimaryKey(tpDTO.getNumberOfSeats());
            result = MyUtil.countPrice(tpDTO.getPrice(), tpDTO.getLimitTime(), duration, ccDTO.getSurchargeHr(), discount);
            txtPrice.setText(Integer.toString(result));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

