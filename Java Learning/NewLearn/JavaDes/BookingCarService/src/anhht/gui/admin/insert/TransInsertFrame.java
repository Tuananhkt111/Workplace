    private void btnBookActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBookActionPerformed
        // TODO add your handling code here:
        boolean check = true;
        TravelTransactionDAO ttDAO = new TravelTransactionDAO();
        String transID, travelID = null, carSerial = null, empID = null;
        Timestamp startTime;
        int duration, price = 0;
        //Get ID transaction        
        transID = Long.toString(new Date().getTime());
        //Get travelID
        try {
            int rowTblTravelPrice = tblViewTravelPrice.getSelectedRow();
            if (rowTblTravelPrice == -1) {
                check = false;
            }
            travelID = (String) tblModelTravelPrice.getValueAt(rowTblTravelPrice, 0);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Please select travel destination!");
        }
        //Get Car serial
        try {
            int rowTblCar = tblViewCar.getSelectedRow();
            if (rowTblCar == -1) {
                check = false;
            }
            carSerial = (String) tblModelCar.getValueAt(rowTblCar, 0);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Please select car!");
        }
        //Get empID
        try {
            int rowTblEmp = tblViewEmployee.getSelectedRow();
            if (rowTblEmp == -1) {
                check = false;
            }
            empID = (String) tblModelEmployee.getValueAt(rowTblEmp, 0);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Please select employee!");
        }
        //Get start time
        Date date = (Date) spStartTime.getValue();
        startTime = new Timestamp(date.getTime());
        //Get duration
        duration = (Integer) spDuration.getValue();
        //Get price
        try {
            if (txtPrice.getText().equals("")) {
                check = false;
            }
            price = Integer.parseInt(txtPrice.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Please count the price!");
        }
        if (check) {
            if (ttDAO.insert(new TravelTransactionDTO(transID, travelID, cusID, empID, carSerial, price, duration, startTime))) {
                JOptionPane.showMessageDialog(null, "Book car success");
                btnDeleteMain.setEnabled(false);
                btnSearchMain.doClick();
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Book car failed!");
            }
        }
    }//GEN-LAST:event_btnBookActionPerformed

