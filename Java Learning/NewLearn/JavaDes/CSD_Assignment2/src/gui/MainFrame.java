    private void btnTransActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTransActionPerformed
        // TODO add your handling code here:
        String mode = (String) cbChooser.getSelectedItem();
        if (txtText.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Please enter a word");
        } else {
            switch (mode) {
                case "VIE - ENG":
                    Node p = wm.vie_engTree.search(wm.vie_engTree.getRoot(), txtText.getText());
                    if (p != null) {
                        MyLinkedList mll = p.getMeaningList();
                        txtTextTrans.setText(mll.traverse());
                    } else {
                        JOptionPane.showMessageDialog(null, "Not found meanings");
                    }
                    break;
                case "ENG - VIE":
                    Node p2 = wm.eng_vieTree.search(wm.eng_vieTree.getRoot(), txtText.getText());
                    if (p2 != null) {
                        MyLinkedList mll = p2.getMeaningList();
                        txtTextTrans.setText(mll.traverse());
                    } else {
                        JOptionPane.showMessageDialog(null, "Not found meanings");
                    }
                    break;
            }
        }
    }//GEN-LAST:event_btnTransActionPerformed

