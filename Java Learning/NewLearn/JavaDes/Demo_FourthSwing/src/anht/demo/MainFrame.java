    private void btnOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOKActionPerformed
        // TODO add your handling code here:
        if(validData()) {
            //Accessing data from input components
            String s = "Welcome ";
            s += this.txtCode.getText() + ", ";
            s += this.txtName.getText() + ", ";
            s += (this.rdMale.isSelected()? "Male, " : "Female, ");
            s += this.txtEmail.getText() + ", ";
            s += this.cbbDegree.getSelectedItem() + ", ";
            s += this.txtaExp.getText() + ", ";
            s += (this.cbMusic.isSelected()? "Music, " : "");
            s += (this.cbSport.isSelected()? "Sport, " : "");
            JOptionPane.showMessageDialog(this, s);
        }
    }//GEN-LAST:event_btnOKActionPerformed
    //checking whether data is valid or not
    private boolean validData() {
        String s;
        //Checking code format: E000
        s = this.txtCode.getText().trim().toUpperCase();
        this.txtCode.setText(s);
        if (!s.matches("^E\\d{3}$")) {
            JOptionPane.showMessageDialog(this, "Code: E + 3 digits.");
            this.txtCode.requestFocus();
            return false;
        }
        //Name is required
        s = this.txtName.getText().trim().toUpperCase();
        this.txtName.setText(s);
        if (s.length() == 0) {
            JOptionPane.showMessageDialog(this, "Name is required.");
            this.txtName.requestFocus();
            return false;
        }
        //Checking E-mail
        s = this.txtEmail.getText().trim();
        this.txtEmail.setText(s);
        if (!s.matches("^\\w+@\\w+[.]\\w+$")) {
            JOptionPane.showMessageDialog(this, "Email: anyname@any.any");
            this.txtEmail.requestFocus();
            return false;
        }
        //Degree must be selected
        if(cbbDegree.getSelectedIndex() <= 0) {
            JOptionPane.showMessageDialog(this, "Degree must be selected.");
            this.cbbDegree.requestFocus();
            return false;
        }
        //experiencies cannot be required
        //hobbies cannot be required
        return true;
    }

