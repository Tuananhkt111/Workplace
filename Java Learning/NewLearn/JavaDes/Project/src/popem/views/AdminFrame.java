    private void btnSearchAccountActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchAccountActionPerformed
        // TODO add your handling code here:
        String attribute = cbSearchAccount.getSelectedItem().toString();
        String keyword = txtSearchAccount.getText();
        String validate = "";
        
        if(keyword.equals("") && !attribute.equals("All")) validate += "Search keyword can't be blank!";
        if (attribute.equals("Character")) attribute = "Character_name";
        if (attribute.equals("Class")) attribute = "Character_Class";
        if (!validate.equals("")){
            JOptionPane.showMessageDialog(null, validate);
        } else {
            try {
                AccountDAO dao = new AccountDAO();
                List<AccountDTO> list = dao.findByAttribute(attribute, keyword);
                showAccount(list);
                txtSearchMission.setText("");
                if (list.size()==0) JOptionPane.showMessageDialog(this, "Not found!");
            } catch (Exception e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "Search failed!");
            }
        }
    }//GEN-LAST:event_btnSearchAccountActionPerformed

    private void tblAccountMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblAccountMouseClicked
        // TODO add your handling code here:
        int row = tblAccount.getSelectedRow();
        String username = (String)tblModelAccount.getValueAt(row, 3);
        String characterClass = (String)tblModelAccount.getValueAt(row, 2);
        String characterRank = (String)tblModelAccount.getValueAt(row, 1);
        String characterName = (String)tblModelAccount.getValueAt(row, 0);
        AccountDAO aDAO = new AccountDAO();
        ItemDAO iDAO = new ItemDAO();
        InventoryDAO inDAO = new InventoryDAO();
        try {
            AccountDTO dto = aDAO.findByPrimaryKey(username);
            txtAccountUsername.setText(username);
            txtAccountUsername.setEditable(false);
            cbAccountRank.setSelectedItem(characterRank);
            txtAccountCharName.setText(characterName);
            cbAccountCharacterClass.setSelectedItem(characterClass);
            txtAccountPassword.setText(dto.getPassword());
            txtAccountPoints.setText(dto.getPoints()+"");
            cbAccountRole.setSelectedItem(dto.getRole());
            lblItemName.setText("Item");
            lblDes.setText(" ");
            lblDropRate.setText(" ");
            List<InventoryDTO> inventory = inDAO.findByUsername(username);
            if (inventory!=null) {
                for (InventoryDTO item : inventory){
                    item.setItem(item.getItem()+"-"+new ItemDAO().findByPrimaryKey(item.getItem()).getName());
                }
            }
            loadInventory(inventory);
        } catch (Exception e) {
            e.printStackTrace();
        }
        btnAccountDelete.setEnabled(true);
        btnAccountUpdate.setEnabled(true);
        btnAccountCreate.setEnabled(false);
    }//GEN-LAST:event_tblAccountMouseClicked

