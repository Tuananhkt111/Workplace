    private void btnSearchMissionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchMissionActionPerformed
        // TODO add your handling code here:
        String attribute = cbSearchMission.getSelectedItem().toString();
        String keyword = txtSearchMission.getText();
        String validate = "";
        
        if(keyword.equals("") && !attribute.equals("All")) validate += "Search keyword can't be blank!";
        if (!validate.equals("")){
            JOptionPane.showMessageDialog(null, validate);
        } else {
            try {
                MissionDAO dao = new MissionDAO();
                List<MissionDTO> list = dao.findByAttribute(attribute, keyword);
                showMission(list);
                txtSearchMission.setText("");
                if (list.size()==0) JOptionPane.showMessageDialog(this, "Not found!");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Search failed!");
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnSearchMissionActionPerformed

    private void tblMissionMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblMissionMouseClicked
        // TODO add your handling code here:
        String id = (String)tblModelMission.getValueAt(tblMission.getSelectedRow(), 0);
        MissionDAO missionDAO = new MissionDAO();
        ItemDAO itemDAO = new ItemDAO();
        try {
            MissionDTO dto = missionDAO.findByPrimaryKey(id);
            txtMissionAnswer.setText(dto.getAnswer());
            txtMissionName.setText(dto.getName());
            cbMissionRank.setSelectedItem(dto.getRank()+"");
            txtMissionChoice1.setText(dto.getChoice1());
            txtMissionChoice2.setText(dto.getChoice2());
            txtMissionChoice3.setText(dto.getChoice3());
            txtMissionID.setText(id);
            cbMissionType.setSelectedItem(dto.getType());
            txtMissionPoint.setText(dto.getPoints()+"");
            txtMissionQuestion.setText(dto.getQuestion());
            cbMissionItem.setSelectedItem(itemDAO.findByPrimaryKey(dto.getItem()).toString());
            txtMissionID.setEditable(false);
        } catch (Exception ex) {
            Logger.getLogger(AdminFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        btnMissionDelete.setEnabled(true);
        btnMissionUpdate.setEnabled(true);
        btnMissionCreate.setEnabled(false);
    }//GEN-LAST:event_tblMissionMouseClicked

