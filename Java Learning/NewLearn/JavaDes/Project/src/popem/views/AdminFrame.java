    private void btnAccountCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAccountCreateActionPerformed
        // TODO add your handling code here:
        String validate = "";
        if(txtAccountUsername.getText().equals("")) validate += "Username can't be blank!\n";
        if(txtAccountCharName.getText().equals("")) validate += "Character name can't be blank!\n";
        if(new String(txtAccountPassword.getPassword()).equals("")) validate += "Password can't be blank!\n";
        if(txtAccountPoints.getText().equals("")) validate += "Points name can't be blank!\n";
        else {
            try {
                if (Integer.parseInt(txtAccountPoints.getText())<0) validate += "Point must be positive number!\n";
            } catch (Exception e) {
            validate += "Point must be number!\n";
            }
        }
        AccountDAO dao = new AccountDAO();
        try {
            if (dao.findByPrimaryKey(txtAccountUsername.getText())!=null) validate+= "Username Existed!\n";
        } catch (Exception ex) {
            Logger.getLogger(AdminFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (!validate.equals("")) JOptionPane.showMessageDialog(this, validate);
        else {
            
            AccountDTO dto = new AccountDTO(txtAccountUsername.getText(), new String(txtAccountPassword.getPassword())
                    , cbAccountRank.getSelectedItem().toString(), cbAccountRole.getSelectedItem().toString()
                    , cbAccountCharacterClass.getSelectedItem().toString()
                    , txtAccountCharName.getText(), Integer.parseInt(txtAccountPoints.getText()));
            try {
                if (dao.create(dto)) JOptionPane.showMessageDialog(this, "Create Successful");
                else JOptionPane.showMessageDialog(this, "Create Failed");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Create Failed");
            }
        }
        
    }//GEN-LAST:event_btnAccountCreateActionPerformed

