    private Thread Effect(){
        return new Thread(){
            @Override
            public void run() {
                while (true) {
                    try {
                        for (int i = 0; i < 11; i++) {
                            Effect.setLocation(Effect.getLocation().x+40, Effect.getLocation().y);
                            sleep(500);
                        }
                        sleep(1000);
                        for (int i = 0; i < 110; i++){
                            Effect.setLocation(Effect.getLocation().x-4, Effect.getLocation().y);
                            sleep(3);
                        }
                        for (int i = 0; i < 10; i++){
                            Effect.setVisible(false);
                            sleep(50);
                            Effect.setVisible(true);
                            sleep(50);
                        }
                        
                    } catch (InterruptedException ex) {
                        Logger.getLogger(AdminFrame.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
                    
                }
            };
    }
    
    private void showMission(List<MissionDTO> list) throws Exception{
        tblModelMission.setRowCount(0);
        for (MissionDTO dto : list){
            tblModelMission.addRow(dto.toVector());
        }
    }

    private void showAccount(List<AccountDTO> list){
        tblModelAccount.setRowCount(0);
        for (AccountDTO dto : list){
            tblModelAccount.addRow(dto.toVector());
        }
    }

    private void loadInventory(List<InventoryDTO> list){
        tblModelAccountInventory.setRowCount(0);
        for (InventoryDTO item : list){
            tblModelAccountInventory.addRow(item.toVector());
        }
    }
    
    private void loadItem(List<String> list){
        for (String string : list){
            cbMissionItem.addItem(string);
        }
    }
    

