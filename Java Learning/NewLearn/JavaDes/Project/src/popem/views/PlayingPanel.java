    private void loadMission(List<MissionDTO> list) throws Exception{
        tblModelMission.setRowCount(0);
        for (MissionDTO dto : list){
            tblModelMission.addRow(dto.toVector());
        }
    }
    
    private void loadCompletedMission(List<CompletedMissionDTO> list) throws Exception{
        CompletedMissionDAO cmDAO = new CompletedMissionDAO();
        MissionDAO mDAO = new MissionDAO();
        Vector temp;
        String missionName;
        tblModelCompletedMission.setRowCount(0);
        for (CompletedMissionDTO cmDTO : list){
            temp = cmDTO.toVector();
            missionName = mDAO.findByPrimaryKey(cmDTO.getMission()).getName();
            temp.set(1, missionName);
            tblModelCompletedMission.addRow(temp);
        }
    }
    
    private void loadInventory(List<InventoryDTO> list){
        tblModelAccountInventory.setRowCount(0);
        for (InventoryDTO item : list){
            tblModelAccountInventory.addRow(item.toVector());
        }
    }
    
    private void loadCbItem() throws Exception{
        ItemDAO dao = new ItemDAO();
        cbMissionItem.removeAllItems();
        cbMissionItem.addItem("None");
        for (InventoryDTO dto : inventoryDTOs){
            if (!dto.getItem().matches("I0[1-3]")){
                String string = dto.getItem()+"-"+dao.findByPrimaryKey(dto.getItem()).getName();
                cbMissionItem.addItem(string); 
            }
        }
    }

