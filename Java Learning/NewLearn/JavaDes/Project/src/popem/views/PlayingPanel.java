    private void cbMissionItemItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbMissionItemItemStateChanged
        // TODO add your handling code here:
        Object item = cbMissionItem.getSelectedItem();
        String itemID = item == null ? "None" : item.toString().split("-")[0];
        if (itemID.equals("None")){
            currentItem = new ItemDTO();
            lblDes.setText(" ");
            lblRemaining.setText(" ");
        } else {
            ItemDAO itemDAO = new ItemDAO();
            InventoryDAO inventoryDAO = new InventoryDAO();
            ItemDTO dto;
            try {
                dto = itemDAO.getGameplayStats(itemID);
                currentItem = dto;
                dto = itemDAO.findByPrimaryKey(itemID);
                lblDes.setText(dto.getDescription());
                lblRemaining.setText("Remaining: "+inventoryDAO.findByPrimaryKey(accountDTO.getUsername(), itemID).getQuantity());
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Error retrieving item info!");
            }
        }


    }//GEN-LAST:event_cbMissionItemItemStateChanged

