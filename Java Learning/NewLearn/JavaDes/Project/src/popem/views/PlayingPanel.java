    private void jTabbedPane1StateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_jTabbedPane1StateChanged
        // TODO add your handling code here:
        if (jTabbedPane1.getSelectedIndex()== 2){ // load leaderboard
            try {
                AccountDAO accountDAO = new AccountDAO();
                List<AccountDTO> list = accountDAO.findTopPlayers();
                int i = 1;
                Vector temp;
                tblModelLeaderboard.setRowCount(0);
                for (AccountDTO accountDTO : list){
                    if (i == 20) break;
                    if (accountDTO.getRole().equals("user") && accountDTO.getCharacterName()!=null){
                        temp = accountDTO.toVectorLeaderboard();
                        temp.set(0, i++);
                        tblModelLeaderboard.addRow(temp);
                    }
                }
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(pnlRanking, "Error loading leaderboard!");
            }
        } else if (jTabbedPane1.getSelectedIndex() == 3){
            try {
                accountDTO = new AccountDAO().findByPrimaryKey(accountDTO.getUsername());
                CompletedMissionDAO cmDAO = new CompletedMissionDAO();
                cbAccountCharacterClass.setSelectedItem(accountDTO.getCharacterClass());
                txtAccountUsername.setText(accountDTO.getUsername());
                txtAccountCharName.setText(accountDTO.getCharacterName());
                txtAccountPassword.setText(accountDTO.getPassword());
                txtAccountPoints.setText(accountDTO.getPoints()+"");
                cbAccountRank.setSelectedItem(accountDTO.getRank());
                cbAccountRole.setSelectedItem(accountDTO.getRole());
                loadCompletedMission(cmDAO.findByUsername(accountDTO.getUsername()));
                inventoryDTOs = new InventoryDAO().findByUsername(accountDTO.getUsername());
                if (inventoryDTOs!=null) {
                    for (InventoryDTO item : inventoryDTOs){
                        item.setItem(item.getItem()+"-"+new ItemDAO().findByPrimaryKey(item.getItem()).getName());
                    }
                }
                loadInventory(inventoryDTOs);
            } catch (Exception ex) {
                Logger.getLogger(PlayingPanel.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else if (jTabbedPane1.getSelectedIndex() == 0){
            try {
                inventoryDTOs = new InventoryDAO()
                        .findByUsername(accountDTO.getUsername());
                loadCbItem();
            } catch (Exception ex) {
            }
        }
    }//GEN-LAST:event_jTabbedPane1StateChanged

