    private void btnAccountUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAccountUpdateActionPerformed
        // TODO add your handling code here:
        String validate = "";
        boolean classChanged = false, nameChanged = false;
        String className = cbAccountCharacterClass.getSelectedItem().toString();
        String characterName = txtAccountCharName.getText();
        if(characterName.equals("")) validate += "Character name can't be blank!\n";
        else if (!characterName.matches("\\w{6,12}")) validate += "Character name must be 6-12 characters!\n";
        if(!accountDTO.getCharacterClass().equals(cbAccountCharacterClass.getSelectedItem().toString())){
            accountDTO.setCharacterClass(cbAccountCharacterClass.getSelectedItem().toString());
            classChanged = true;
        }
        if (!accountDTO.getCharacterName().equals(txtAccountCharName.getText())) {
            accountDTO.setCharacterName(txtAccountCharName.getText());
            nameChanged = true;
        }
        
        if (!validate.equals("")) JOptionPane.showMessageDialog(this, validate);
        else {
            if (classChanged || nameChanged) {
                try {
                    AccountDAO dao = new AccountDAO();
                    if (dao.update(accountDTO)) {
                        if (classChanged) {
                            userFrame.getLblCharacterAvatar().setIcon(new ImageIcon(getClass().getResource("/popem/img/"+accountDTO.getCharacterClass()+".png")));
                            userFrame.getLblCharacterAvatarBG().setIcon(new ImageIcon(getClass().getResource("/popem/img/"+accountDTO.getCharacterClass()+"_bg.png")));
                        }
                        JOptionPane.showMessageDialog(this, "Update Successful");
                    }
                    else JOptionPane.showMessageDialog(this, "Update Failed");
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, "Error updating account!");
                }
            }
        }

    }//GEN-LAST:event_btnAccountUpdateActionPerformed

