    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        //1.Nhan request tu client hoac controller khac
        //Nhan fullname va yeu cau search tu phia client
        String search = txtFullname.getText();
        //2.Goi model
        //goi RegistrationDTO
        RegistrationDAO dao = new RegistrationDAO();
        //goi ham findFileByName tu DTO, nhan ket qua tra ve tu model la 1 list DTO 
        List<RegistrationDTO> result = dao.findByName(search);
        //3.Dua ket qua tuong ung: List, mot kieu du lieu gi do hoac Exception
        showData(result);
    }//GEN-LAST:event_btnSearchActionPerformed

    private void tblViewRegistrationMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblViewRegistrationMouseClicked
        // TODO add your handling code here:
        //1.Nhan request tu client hoac controller khac
        //--Nhan username tu table 
        int row = tblViewRegistration.getSelectedRow();
        String username = (String) tblViewRegistration.getValueAt(row, 0);
        //2. Goi model
        RegistrationDAO dao = new RegistrationDAO();
        RegistrationDTO dto = dao.findByPrimaryKey(username);
        
        //--goi ham findByPrimaryKey tu RegstrationDTO
        //--nhan kq tra ve la DTO
        txtFullname.setText(dto.getFullname());
        txtUsername.setText(dto.getUsername());
        if(dto.getRole().equals("Admin")) {
            rdAdmin.setSelected(true);
        } else {
            rdUser.setSelected(true);
        }
        txtUsername.setEditable(false);
        
    }//GEN-LAST:event_tblViewRegistrationMouseClicked

