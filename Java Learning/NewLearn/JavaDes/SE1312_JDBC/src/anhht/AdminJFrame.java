    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertActionPerformed
        try {
            // TODO add your handling code here:
            //1.Nhan request client hoac server khac
            //--Nhan username, pass, fullname, role tu insert cua client
            String username = txtUsername.getText();
            String fullname = txtFullname.getText();
            String password = new String(txtPass.getPassword());
            String role = "User";
            if(rdAdmin.isSelected()) {
                role = "Admin";
            }
            RegistrationDTO dto = new RegistrationDTO(username, fullname, role);
            dto.setPassword(password);
            //2.goi model
            RegistrationDAO dao = new RegistrationDAO();
            boolean check = dao.insert(dto);
            //3.xu li het qua
            if(check) {
                JOptionPane.showMessageDialog(null, "Insert Success");
                tblModelRegistration.addRow(dto.toVector());
            } else {
                JOptionPane.showMessageDialog(null, "Insert Failed!");
            }
        } catch (Exception ex) {
            if(ex.getMessage().contains("duplicate")) {
                JOptionPane.showMessageDialog(null, "Username is existed");
            }
            Logger.getLogger(AdminJFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btnInsertActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        try {
            // TODO add your handling code here:
            //1.Nhan request client hoac server khac
            //--Nhan username, pass, fullname, role tu insert cua client
            String username = txtUsername.getText();
            String fullname = txtFullname.getText();
            String password = new String(txtPass.getPassword());
            String role = "User";
            if(rdAdmin.isSelected()) {
                role = "Admin";
            }
            RegistrationDTO dto = new RegistrationDTO(username, fullname, role);
            dto.setPassword(password);
            //2.goi model
            RegistrationDAO dao = new RegistrationDAO();
            boolean check = dao.update(dto);
            if(check) {
                JOptionPane.showMessageDialog(null, "Update success");
                int row = tblViewRegistration.getSelectedRow();
                tblModelRegistration.setValueAt(fullname, row, 1);
                tblModelRegistration.setValueAt(role, row, 2);
            } else {
                JOptionPane.showMessageDialog(null, "Update failed!");
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
            Logger.getLogger(AdminJFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        try {
            // TODO add your handling code here:
            //Nhan username va yeu cau delete tu phia client
            int row = tblViewRegistration.getSelectedRow();
            String username = (String) tblModelRegistration.getValueAt(row, 0);
            //3. goi model
            RegistrationDAO dao = new RegistrationDAO();
            boolean check = dao.delete(username);
            if(check) {
                JOptionPane.showMessageDialog(null, "Delete success");
                tblModelRegistration.removeRow(row);
            } else {
                JOptionPane.showMessageDialog(null, "Delete Failed");
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
            Logger.getLogger(AdminJFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

