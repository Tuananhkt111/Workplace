    private void tblViewStudentMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblViewStudentMouseClicked
        // TODO add your handling code here:
        int row = tblViewStudent.getSelectedRow();
        String id = (String) tblModelStudent.getValueAt(row, 0);
        String name = (String) tblModelStudent.getValueAt(row, 1);
        String gender = (String) tblModelStudent.getValueAt(row, 2);
        float mark = (Float) tblModelStudent.getValueAt(row, 3);

        jTextField1.setText(id);
        jTextField2.setText(name);
        if (gender.equals("Male")) {
            jRadioButton1.setSelected(true);
        }
        if (gender.equals("Female")) {
            jRadioButton2.setSelected(true);
        }
        jTextField3.setText(mark + "");

        jTextField1.setEditable(false);
        jTextField2.setEditable(false);
        jTextField3.setEditable(false);
    }//GEN-LAST:event_tblViewStudentMouseClicked

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int row = tblViewStudent.getSelectedRow();
        String name = jTextField2.getText().trim();
        String textMark = jTextField3.getText().trim();
        String gender = "Male";
        if (jRadioButton2.isSelected()) {
            gender = "Female";
        }
        tblModelStudent.setValueAt(gender, row, 2);
        tblModelStudent.setValueAt(name, row, 1);
        tblModelStudent.setValueAt(Float.parseFloat(textMark), row, 3);
    }//GEN-LAST:event_jButton1ActionPerformed

