    private void viewListStudent(List<StudentDTO> list) {
        //hien thi du lieu do model xu li duoc controller day len
        tblModelStudent.setRowCount(0);
        for (StudentDTO studentDTO : list) {
            tblModelStudent.addRow(studentDTO.toVector());
        }
    }

    private void FirstController() {
        //1. Nhan request tu client hoc controller khac

        //2. Goi model
        StudentDAO dao = new StudentDAO();
        // Load du lieu
        List<StudentDTO> list = dao.loadData(fileName);
        //3. Day du lieu len View
        viewListStudent(list);
    }

